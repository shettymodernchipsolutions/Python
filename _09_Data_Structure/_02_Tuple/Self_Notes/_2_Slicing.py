print("------------- Slicing ---------------")

tup = (10, 20, 30, 40, 50, 60, 70)

print("\nBefore Slicing: ", tup)

print("\nAfter Slicing: ", tup[0:3])

print("\nSpecifying only the initial value: ", tup[3:])

print("\nSpecifying only the ending value: ", tup[:4])

print("\nTuple element in reverse order: ", tup[::-1])

print("\nSlicing in reverse order: ", tup[-2:-5:-1])

print("\nSlicing in reverse order specifying only the initial values: ", tup[-4::-1])

print("\nSlicing in reverse order specifying only the ending values: ", tup[:-4:-1])

